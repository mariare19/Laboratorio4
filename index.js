const x = 5;
const y = 10;

function sumaNumeros() {
    return x + y;
}

function restaNumeros() {
    let i = 10;
    let j = 3;
    return i - j;
}

function multiplicarNumeros(g, h) {
    return g*h;
}

console.log("Suma de Numeros: " + sumaNumeros());
console.log("Resta de Numeros: " + restaNumeros());
console.log("Multiplicacion de Numeros: " + multiplicarNumeros(100, 2));